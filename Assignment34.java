package org.grotech.assignments;

// Write a program using a switch case with the help of a for loop.

public class Assignment34 {
	
	public static void main(String[] args) {
		for(int i=1;i<=6;i++) {
		switch (i) {
		case 1:System.out.println("chrome browser");
			break;
		case 2:System.out.println("firefox");	
			break;
		case 3:System.out.println("IE");
			break;
		case 4: System.out.println("Safari");	
		    break;
		case 5: System.out.println("microsoft");
		    break;
		default:System.out.println("Enter your choice:");
			
		}

	}

}
}
