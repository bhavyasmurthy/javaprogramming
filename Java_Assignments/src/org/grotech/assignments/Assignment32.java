package org.grotech.assignments;

// Write a Program on Switch Concept
public class Assignment32 {

	public static void main(String[] args) {// char a ='a' switch('a) case 'a'
		switch (12) {
		case 1:
			System.out.println(" switch to 1");
			break;
		case 2:
			System.out.println("switch to 2");
			break;
		case 3:
			System.out.println("switch to 3");
			break;
		case 4:
			System.out.println("switch to 4");
			break;
		case 5:
			System.out.println("switch to 5");
			break;
		default:
			System.out.println("Enter your choice from 1 to 5");

		}
	}

}
